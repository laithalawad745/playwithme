// دالة للحصول على سؤال عشوائي
export const getRandomQuestion = (usedQuestions = []) => {
  const availableQuestions = findCountryQuestions.filter(q => !usedQuestions.includes(q.id));
  if (availableQuestions.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * availableQuestions.length);
  return availableQuestions[randomIndex];
};

// دالة للحصول على أسئلة حسب القارة
export const getQuestionsByContinent = (continent) => {
  return findCountryQuestions.filter(q => q.continent === continent);
};

// دالة للحصول على إحصائيات
export const getQuestionsStats = () => {
  const continents = {};
  findCountryQuestions.forEach(q => {
    if (!continents[q.continent]) {
      continents[q.continent] = 0;
    }
    continents[q.continent]++;
  });
  
  return {
    total: findCountryQuestions.length,
    continents: continents
  };
};
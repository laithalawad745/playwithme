export const getAllBettingQuestions = () => {
  const allQuestions = [];
  Object.values(bettingGameData).forEach(category => {
    allQuestions.push(...category.questions);
  });
  return allQuestions;
};

export const getRandomBettingQuestion = (usedQuestionIds = []) => {
  const allQuestions = getAllBettingQuestions();
  const availableQuestions = allQuestions.filter(q => !usedQuestionIds.includes(q.id));
  
  if (availableQuestions.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * availableQuestions.length);
  return availableQuestions[randomIndex];
};

export const getBettingQuestionsByDifficulty = (difficulty) => {
  const allQuestions = getAllBettingQuestions();
  return allQuestions.filter(q => q.difficulty === difficulty);
};

export const getBettingStatistics = () => {
  const stats = {};
  Object.keys(bettingGameData).forEach(categoryId => {
    const questions = bettingGameData[categoryId].questions;
    stats[categoryId] = {
      total: questions.length,
      easy: questions.filter(q => q.difficulty === 'easy').length,
      medium: questions.filter(q => q.difficulty === 'medium').length,
      hard: questions.filter(q => q.difficulty === 'hard').length
    };
  });
  return stats;
};

